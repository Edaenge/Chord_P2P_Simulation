    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jToggleButton1ActionPerformed
    {//GEN-HEADEREND:event_jToggleButton1ActionPerformed
        // TODO add your handling code here:
        if(jToggleButton1.isSelected())
        {
            String bits =  jTextField1.getText();
            String fingers = jTextField2.getText();
            
            mManager = new ChordManager(Integer.parseInt(bits), Integer.parseInt(fingers));
            
            jToggleButton1.setText("Stop");
            jTextField3.setEnabled(true);
            jTextField4.setEnabled(true);
            jButton1.setEnabled(true);
            jButton2.setEnabled(true);
            jButton3.setEnabled(true);
            jButton4.setEnabled(true);
            jButton5.setEnabled(true);
            jButton6.setEnabled(true);
            jButton7.setEnabled(true);
            
            this.mMaintenanceTasks = new ScheduledThreadPoolExecutor(1,
                        new GUI.GUIThreadFactory("Circle Stabilize Check"));
            
            // start thread which periodically stabilizes with successor
            this.mMaintenanceTasks.scheduleWithFixedDelay(
                    new StabilityCheck(mManager.GetCircleManager(), jLabel7)
                    , 8, 8, TimeUnit.SECONDS);
            
             
        }
        else
        {
            this.mMaintenanceTasks.shutdownNow();
            this.mMaintenanceTasks = null;
            
            jTextArea1.setText("");
            jToggleButton1.setText("Start");
            mManager.KillAll();
            mManager = null;
            jComboBox1.removeAllItems();
            jTextField3.setEnabled(false);
            jTextField4.setEnabled(false);
            jButton1.setEnabled(false);
            jButton2.setEnabled(false);
            jButton3.setEnabled(false);
            jButton4.setEnabled(false);
            jButton5.setEnabled(false);
            jButton6.setEnabled(false);
            jButton7.setEnabled(false);
            jLabel3.setText("0");
            jLabel5.setText("0");
            jLabel7.setText("0%");
            jPanel2.setEnabled(false);

        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButton1ActionPerformed
    {//GEN-HEADEREND:event_jButton1ActionPerformed
        // TODO add your handling code here:

        if( jTextField3.getText().isEmpty() )
            jTextField3.setText("1");
        
        Thread t = new Thread(new Runnable()
        {

            @Override
            public void run()
            {
                int size = Integer.parseInt(jTextField3.getText());
                String n;
            
                for(int i = 0; i<size; i++)
                {
                    n = mManager.CreateNode();

                    if(n != null)
                        jComboBox1.addItem(n);
                    else
                        break;

                    try {
                        Thread.sleep(50);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, ex);
                    }
                } 
                
                jLabel3.setText(mManager.GetSize()+"");
            }
            
        });
        
        t.start();
        
    }//GEN-LAST:event_jButton1ActionPerformed

