    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt)//GEN-FIRST:event_jTextField1KeyReleased
    {//GEN-HEADEREND:event_jTextField1KeyReleased
        // TODO add your handling code here: 
        int val, val2;
        
        try {
            val = Integer.parseInt(jTextField1.getText());
            val2 = Integer.parseInt(jTextField2.getText());
        } catch (NumberFormatException ex) {
            return;
        }
        
        if( val <= 0 || val >16 )
        {
            jTextField1.setText("16");
            jTextField2.setText("16");
        }
        else if(val2 > val)
        {
            jTextField2.setText(Integer.toString(val));
        }
        else
            jTextField2.setText(Integer.toString(val));
    }//GEN-LAST:event_jTextField1KeyReleased

